        <p>
           Introducción: En esta ocasión se estudiará algunos símbolos del bautismo, con el propósito de que toda persona que desee realizar su bautismo esté consciente de lo que esto significa, para que su vida cristiana sea un verdadero éxito.
            <br>
            <h3>1.- BAUTIZARSE SIGNIFICA MORIR</h3>

            La palabra “Bautizar” se deriva del latín “Baptizarre” que significa “sumergir”. Por esta razón al momento de bautizar a una persona se le sumerge totalmente en el agua, es decir se le sepulta, Romanos 6:4.
            Pero este término de morir no debe aplicarse únicamente al momento de sumergir a la persona en el agua, verdaderamente la persona debe morir. ¿Qué es lo que muere en el bautismo? Lo que muere es el viejo hombre, la antigua personalidad de pecado, como dice en Efesios 4:22 y 2ª Corintios 5:17.
            Como ya hemos reconocido que nuestra condición es de pecado y nos hemos arrepentido de ese sistema de vida, y ese mismo arrepentimiento ha hecho que nos convirtamos a Dios, esto mismo da por resultado nuestra muerte al pecado. Romanos 6:2.
                Bautizarse significa una muerte total al pecado (Romanos 6:11). El nuevo hombre, es el que debe salir de las aguas bautismales. El hombre que ha sido engendrado por la voluntad de Dios. (Juan 1:13; Efesios 4:24)

            <h3>2º BAUTIZARSE SIGNIFICA HACER UN PACTO</h3>
            La palabra pacto quiere decir: “Acuerdo entre dos partes: Dios y la persona” Gálatas 3:15
            En este caso del bautismo, una parte es Dios y otra la persona, Dios en su plan Redentor se compromete:

            




            <h4>a)	Perdonar sus pecados (1ª Juan 2:12).</h4>
            <h4>b)	Participar del Don del Espíritu Santo y darle la vida eterna. Hechos 10:43-45; 1ª Juan 2:25.</h4>
            USTED QUEDA OBLIGADO A:
            <h4>a)	Aceptar a Jesucristo (Juan 1:12</h4>
            <h4>b)	Obedecer la fe (Romanos 16:26)</h4>
            <h4>c)	Estar firme hasta el fin (Hebreos 3:14)</h4>

            Al bautizarse usted ingresa al nuevo pacto realizado por Cristo en la cruz del calvario. (Mat. 26:28)
A partir de su bautismo no debe poner la mirada en los hombres, porque ellos están llenos de defectos. 
Jamás tendrá razón si quebranta el pacto por motivo de los humanos. Hebreos 12:2

No ponga la mirada en quien le evangelizó; ni en el Ministro de la Iglesia, ni en ningún miembro manténgase viendo al invisible. Hebreos 11:26-27
Recuerde que en este pacto Dios es fiel a su palabra y jamás la quebrantará. 2ª Timoteo 2:13

Ahora sólo resta que usted sea fiel cumpliendo con el compromiso adquirido delante de Dios.


        

            
