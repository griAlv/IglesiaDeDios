        <p>
            La mayoría de grupos religiosos no están de acuerdo en guardar los mandamientos de Dios,
            según la interpretación que dan a <em>Efesios 2:8</em>, olvidando lo que dice <em>1ª Juan 2:3-4</em>.
            Además, muchas agrupaciones religiosas enseñan que la salvación no se pierde a pesar de cometer pecado,
            basándose en <em>1ª Juan 2:1</em>, pero olvidan lo que dice <em>Apocalipsis 3:5</em>.
            Este sistema de creer da por resultado pastores adúlteros, fornicarios, mundanos, etc.
            Estos mismos son los que forman nuevas agrupaciones.
            Estos son los pastores que bautizan a personas sinceras que desean buscar a Dios
            (<em>Proverbios 28:9; Judas verso 4</em>).
        </p>

        <h3>3° ¡Porque por medio del bautismo somos añadidos a la Iglesia de Dios!</h3>

        <p>
            El bautismo que recibimos en esos cuerpos extraños por ministros que violan los mandamientos de Dios,
            no tiene valor. Es necesario ser bautizado en la Iglesia de Dios para ser añadido a ella.
            <em>Hechos 2:41; 1ª Corintios 12:13 y 27.</em>
        </p>

        <p>
            Aun Pablo fue necesario que recibiera el bautismo para que ingresara al cuerpo de la Iglesia.
            <em>Hechos 9:18.</em>
        </p>

