        <p>
            <strong>Introducción:</strong> Nos proponemos en esta lección explicarle al creyente sus deberes para con Dios,
            considerando los puntos más sobresalientes, los cuales detallamos a continuación:
        </p>

        <h3>1º SANTIFICAR EL SÉPTIMO DÍA, SÁBADO PARA DIOS</h3>

        <p>
            Esperamos que esta lección sirva para que usted, como creyente deseoso de obtener su salvación,
            fortalezca su relación con Dios. Por la Palabra de Dios sabemos que quien santificó este día fue el mismo Creador,
            y no encontramos en las Escrituras del Nuevo Pacto un solo versículo que indique que esa santificación y bendición
            se le haya quitado. <em>Génesis 2:1-2.</em>
        </p>

        <p>
            El sábado fue señalado como día de reposo dentro de los Diez Mandamientos que Dios dio a su pueblo.
            <em>Éxodo 20:1-17.</em><br>
            Nuestro Señor explicó que el sábado fue hecho por causa del hombre (<em>Marcos 2:27-28</em>),
            y él mismo cuando estuvo en la tierra también lo guardó. <em>Lucas 4:16.</em><br>
            Por lo tanto, nosotros al hacer pacto con Dios debemos reconocer la santificación y bendición de ese día
            para dedicarlo a Él. <em>Isaías 58:13.</em>
        </p>

        <h4>Formas de cómo santificar el sábado:</h4>
        <ul>
            <li>a) No ocuparse en trabajos remunerados (pagados).</li>
            <li>b) No ocuparse en trabajos o entretenimientos materiales.</li>
            <li>c) No dedicarse a diversiones o paseos.</li>
            <li>d) No hacer viajes con propósitos de paseo.</li>
            <li>e) No hacer compras que estén fuera de necesidades impredecibles.</li>
            <li>f) No dedicarse al aseo general de la casa.</li>
            <li>g) Generalmente, el tiempo debe dedicarse a la asistencia al culto.</li>
            <li>h) Puede dedicarse a visitar enfermos o a la evangelización.</li>
            <li>i) Si no es posible salir de casa, debe ocuparse el tiempo orando y leyendo la Palabra de Dios.</li>
        </ul>

        <h3>2º CONMEMORAR LA MUERTE DEL SEÑOR</h3>

        <p>
            Es una orden de nuestro Señor Jesucristo el hacer memoria de Él por medio de la Cena del Señor.
            <em>Lucas 22:19.</em><br>
            Por lo tanto, se constituye en un deber nuestro celebrar cada año la “Cena del Señor”.
        </p>

        <h4>Forma en que debe celebrarse:</h4>
        <ul>
            <li>
                a) Se busca en un calendario judío el 14 del mes de Nisán. Como el día principia por la parte oscura,
                la fecha exacta para conmemorarla es al finalizar el 13 de Nisán y comenzar el 14.
                Se compara con el calendario gregoriano (civil), sea marzo o abril según el año.
            </li>
            <li>
                b) Al principio del día 14 de Nisán (a las 6 p.m.) debemos hacernos presentes en el culto de la Cena del Señor
                sin faltar por ningún motivo secundario.
            </li>
            <li>
                c) Debemos estar preparados espiritualmente para participar del pan y del vino.
                Esta preparación espiritual debe mantenerse durante todo el año, no solo la noche del 14 de Nisán.
            </li>
            <li>
                d) Nuestro deber es comer del pan ázimo (símbolo del cuerpo de Cristo) y beber el jugo de la vid
                (símbolo de la sangre de Cristo).
            </li>
            <li>
                e) Por medio de este culto damos evidencia de dos cosas:
                <ul>
                    <li>- Anunciamos la muerte de Cristo hasta que venga.</li>
                    <li>- Anunciamos que somos parte del cuerpo de Cristo.</li>
                </ul>
            </li>
        </ul>

        <h3>3º PRACTICAR EL LAVACRO DE LOS PIES</h3>

        <p>
            A continuación, se expone la forma en que debe hacerse:
        </p>

        <ul>
            <li>
                a) Concluida la participación del pan y del vino, las hermanas pasan al lugar preparado
                y enseguida los varones; ambos grupos deben hacerlo separadamente.
            </li>
            <li>
                b) El acto consiste en lavarse los pies mutuamente: hermanas con hermanas y varones con varones.
                Se usa un lavatorio y una toalla.
            </li>
            <li>
                c) Al efectuar el acto se puede orar o decir palabras alusivas al hermano que se lava, por ejemplo:
                <br>
                <em>“Hermano, nuevamente sus pies son lavados en este 14 de Nisán. Procuremos tenerlos limpios,
                porque Jehová aborrece los pies que corren al mal.”</em>
            </li>
            <li>
                d) La Iglesia de Dios practica este mandato porque el Señor Jesús lo ordenó.
                Léase <em>Juan 13:1-17.</em> Este acto es muestra de humildad en la Iglesia de Dios.
                Al ser bautizado, debe practicarlo.
            </li>
        </ul>

        <h3>4º PAGAR AL SEÑOR LOS DIEZMOS</h3>

        <p>
            La costumbre de diezmar la vemos en los hijos de Dios desde tiempos patriarcales.
            Abraham, el padre de la fe, lo practicó. <em>Génesis 14:20.</em><br>
            En el tiempo levítico, Dios explicó que el diezmo es cosa consagrada a Él. <em>Levítico 27:30.</em><br>
            Jesucristo lo confirmó en <em>Mateo 23:23.</em>
        </p>

        <p>
            Desde el momento en que usted ha entendido esta doctrina —dar a Dios lo que es de Dios y a César lo que es del César—
            puede comenzar a cumplir con esto. <em>Mateo 22:21.</em><br>
            A continuación se explica en qué forma se diezma y la finalidad de ello:
        </p>

        <ul>
            <li>a) Diezmar es dar la décima parte de las bendiciones recibidas de parte de Dios.</li>
            <li>b) El diezmo es para el uso de las cosas de Dios en todo sentido.</li>
            <li>
                c) Al diezmar debemos hacerlo como al Señor le agrada y no como el hombre quiera hacerlo.
                Dejar de darlo significa robar a Dios. <em>Malaquías 3:8-10.</em>
            </li>
            <li>
                d) No debe confundirse diezmo con ofrenda. La ofrenda es voluntaria; el diezmo es un mandamiento de Dios
                y debe darse cuando recibimos bendiciones.
            </li>
            <li>
                e) El diezmo puede darse mensualmente, quincenalmente, semanalmente o anualmente,
                según se reciban las bendiciones.
            </li>
            <li>
                f) Para poder cumplir con él, debemos apartarlo inmediatamente y entregarlo a la Iglesia lo más pronto posible
                para evitar gastarlo.
            </li>
            <li>
                g) El diezmo se coloca en el sobre preparado para ello, escribiendo el nombre del que diezma,
                la cantidad y la fecha, todo con claridad, para que el tesorero lleve el control.
            </li>
        </ul>

        <p>
            Este hecho no significa tocar trompeta, sino simplemente seguir un orden establecido.
        </p>

